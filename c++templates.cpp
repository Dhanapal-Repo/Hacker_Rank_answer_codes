/*Write the class AddElements here*/
template <class T>
class AddElements// normal template
{
    public:
    T element;
    AddElements(T e1){element=e1;}
    T add(T ele){return(element+ele);}
};

template<> class AddElements<string> // template speciallization 
{
    public:
    string element;
    AddElements(string arg){element=arg;}
    string concatenate(string ele){return(element+ele);}
};

int main () {
  int n,i;
  cin >> n;
  for(i=0;i<n;i++) {
    string type;
    cin >> type;
    if(type=="float") {
        double element1,element2;
        cin >> element1 >> element2;
        AddElements<double> myfloat (element1);
        cout << myfloat.add(element2) << endl;
    }
    else if(type == "int") {
        int element1, element2;
        cin >> element1 >> element2;
        AddElements<int> myint (element1);
        cout << myint.add(element2) << endl;
    }
    else if(type == "string") {
        string element1, element2;
        cin >> element1 >> element2;
        AddElements<string> mystring (element1);
        cout << mystring.concatenate(element2) << endl;
    }
  }
  return 0;
}
